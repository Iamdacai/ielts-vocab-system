/**
 * 获取随机例句
 */
export function getRandomSentences(count, category = null) {
  let allSentences = [];
  
  if (category) {
    allSentences = IELTS_EXAMPLE_SENTENCES[category] || [];
  } else {
    Object.values(IELTS_EXAMPLE_SENTENCES).forEach(sentences => {
      allSentences = allSentences.concat(sentences);
    });
  }
  
  const shuffled = [...allSentences].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}

/**
 * 根据难度筛选
 */
export function getSentencesByDifficulty(difficulty, category = null) {
  let allSentences = [];
  
  if (category) {
    allSentences = IELTS_EXAMPLE_SENTENCES[category] || [];
  } else {
    Object.values(IELTS_EXAMPLE_SENTENCES).forEach(sentences => {
      allSentences = allSentences.concat(sentences);
    });
  }
  
  return allSentences.filter(s => s.difficulty === difficulty);
}

/**
 * 根据话题筛选
 */
export function getSentencesByTopic(topic, category = null) {
  let allSentences = [];
  
  if (category) {
    allSentences = IELTS_EXAMPLE_SENTENCES[category] || [];
  } else {
    Object.values(IELTS_EXAMPLE_SENTENCES).forEach(sentences => {
      allSentences = allSentences.concat(sentences);
    });
  }
  
  return allSentences.filter(s => s.scene === topic || s.topic === topic);
}

/**
 * 获取同义替换练习
 */
export function getSynonymPractice(word) {
  const synonyms = SYNONYM_REPLACEMENTS[word.toLowerCase()];
  if (!synonyms) return null;
  
  return {
    word,
    synonyms,
    practice: {
      question: `Which word can replace "${word}"?`,
      options: synonyms.map(s => s.word),
    },
  };
}

